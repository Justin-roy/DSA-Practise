#include <iostream>
#include <string>
#include <vector>
using namespace std;

int maxLen(vector<int> A, int n)
{
    int pos = 0, sum = A[0], cnt = 0;
    for (int i = 1; i < n; i++)
    {
        sum += A[i];
        if (sum > 0)
        {
            pos++;
            sum = A[i];
        }
        else
        {
            int diff = (i + 1) - pos;
            cnt = max(cnt, diff);
        }
    }
    return cnt;
}
